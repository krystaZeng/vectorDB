CREATE TABLE IF NOT EXISTS SYS_VECTOR_SYNC_JOBS_ (
    JOB_ID BIGINT NOT NULL,
    COLUMN_ID BIGINT NOT NULL,
    COLLECTION_ID BIGINT,
    INDEX_ID BIGINT,
    JOB_TYPE VARCHAR(16) NOT NULL,
    JOB_STATUS VARCHAR(16) NOT NULL,
    TRIGGER_TYPE VARCHAR(16) NOT NULL,
    IDEMPOTENCY_KEY VARCHAR(128),
    SNAPSHOT_ID VARCHAR(128),
    SOURCE_CURSOR VARCHAR(512),
    START_PK VARCHAR(512),
    END_PK VARCHAR(512),
    WORKER_ID VARCHAR(128),
    ATTEMPT_NO INTEGER NOT NULL,
    RETRY_COUNT INTEGER NOT NULL,
    ERROR_CODE VARCHAR(64),
    ERROR_MESSAGE VARCHAR(2048),
    HEARTBEAT_AT TIMESTAMP,
    HEARTBEAT_AT_EPOCH_MS BIGINT,
    STARTED_AT TIMESTAMP,
    STARTED_AT_EPOCH_MS BIGINT,
    FINISHED_AT TIMESTAMP,
    FINISHED_AT_EPOCH_MS BIGINT,
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    PRIMARY KEY (JOB_ID),
    CONSTRAINT UQ_SYS_VECTOR_SYNC_JOBS_01 UNIQUE (IDEMPOTENCY_KEY),
    CONSTRAINT FK_SYS_VECTOR_SYNC_JOBS_01 FOREIGN KEY (COLUMN_ID) REFERENCES SYS_VECTOR_COLUMNS_ (COLUMN_ID),
    CONSTRAINT FK_SYS_VECTOR_SYNC_JOBS_02 FOREIGN KEY (COLLECTION_ID) REFERENCES SYS_VECTOR_COLLECTIONS_ (COLLECTION_ID),
    CONSTRAINT FK_SYS_VECTOR_SYNC_JOBS_03 FOREIGN KEY (INDEX_ID) REFERENCES SYS_VECTOR_INDEXES_ (INDEX_ID),
    CONSTRAINT CK_SYS_VECTOR_SYNC_JOBS_01 CHECK (JOB_STATUS IN ('PENDING', 'RUNNING', 'SUCCESS', 'FAILED', 'PAUSED', 'CANCELED')),
    CONSTRAINT CK_SYS_VECTOR_SYNC_JOBS_02 CHECK (
        (HEARTBEAT_AT IS NULL AND HEARTBEAT_AT_EPOCH_MS IS NULL)
        OR
        (HEARTBEAT_AT IS NOT NULL AND HEARTBEAT_AT_EPOCH_MS IS NOT NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_SYNC_JOBS_03 CHECK (
        (STARTED_AT IS NULL AND STARTED_AT_EPOCH_MS IS NULL)
        OR
        (STARTED_AT IS NOT NULL AND STARTED_AT_EPOCH_MS IS NOT NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_SYNC_JOBS_04 CHECK (
        (FINISHED_AT IS NULL AND FINISHED_AT_EPOCH_MS IS NULL)
        OR
        (FINISHED_AT IS NOT NULL AND FINISHED_AT_EPOCH_MS IS NOT NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_SYNC_JOBS_05 CHECK (
        (JOB_STATUS = 'PENDING' AND STARTED_AT IS NULL AND STARTED_AT_EPOCH_MS IS NULL AND FINISHED_AT IS NULL AND FINISHED_AT_EPOCH_MS IS NULL)
        OR
        (JOB_STATUS IN ('RUNNING', 'PAUSED') AND STARTED_AT IS NOT NULL AND STARTED_AT_EPOCH_MS IS NOT NULL AND FINISHED_AT IS NULL AND FINISHED_AT_EPOCH_MS IS NULL)
        OR
        (JOB_STATUS IN ('SUCCESS', 'FAILED', 'CANCELED') AND STARTED_AT IS NOT NULL AND STARTED_AT_EPOCH_MS IS NOT NULL AND FINISHED_AT IS NOT NULL AND FINISHED_AT_EPOCH_MS IS NOT NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_SYNC_JOBS_06 CHECK (
        (JOB_STATUS = 'RUNNING' AND HEARTBEAT_AT IS NOT NULL AND HEARTBEAT_AT_EPOCH_MS IS NOT NULL)
        OR
        (JOB_STATUS <> 'RUNNING' AND HEARTBEAT_AT IS NULL AND HEARTBEAT_AT_EPOCH_MS IS NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_SYNC_JOBS_07 CHECK (
        (COLLECTION_ID IS NULL OR COLLECTION_ID > 0)
        AND (INDEX_ID IS NULL OR INDEX_ID > 0)
        AND ATTEMPT_NO >= 0
        AND RETRY_COUNT >= 0
    )
);

CREATE TABLE IF NOT EXISTS SYS_VECTOR_SYNC_PROGRESS_ (
    PROGRESS_ID BIGINT NOT NULL,
    JOB_ID BIGINT NOT NULL,
    COLUMN_ID BIGINT NOT NULL,
    PARTITION_ID VARCHAR(64) NOT NULL,
    LAST_PK VARCHAR(512),
    LAST_EVENT_TIME TIMESTAMP,
    LAST_EVENT_TIME_EPOCH_MS BIGINT,
    LAST_EVENT_ID VARCHAR(128),
    PROCESSED_ROWS BIGINT NOT NULL,
    SUCCESS_ROWS BIGINT NOT NULL,
    FAILED_ROWS BIGINT NOT NULL,
    LAST_BATCH_TIME TIMESTAMP,
    LAST_BATCH_TIME_EPOCH_MS BIGINT,
    PROGRESS_STATUS VARCHAR(16) NOT NULL,
    CHECKPOINT_DATA VARCHAR(2000),
    UPDATED_AT TIMESTAMP NOT NULL,
    PRIMARY KEY (PROGRESS_ID),
    CONSTRAINT UQ_SYS_VECTOR_SYNC_PROGRESS_01 UNIQUE (JOB_ID, PARTITION_ID),
    CONSTRAINT FK_SYS_VECTOR_SYNC_PROGRESS_01 FOREIGN KEY (JOB_ID) REFERENCES SYS_VECTOR_SYNC_JOBS_ (JOB_ID),
    CONSTRAINT FK_SYS_VECTOR_SYNC_PROGRESS_02 FOREIGN KEY (COLUMN_ID) REFERENCES SYS_VECTOR_COLUMNS_ (COLUMN_ID),
    CONSTRAINT CK_SYS_VECTOR_SYNC_PROGRESS_01 CHECK (PROGRESS_STATUS IN ('RUNNING', 'STALLED', 'DONE', 'FAILED')),
    CONSTRAINT CK_SYS_VECTOR_SYNC_PROGRESS_02 CHECK (
        (LAST_EVENT_TIME IS NULL AND LAST_EVENT_TIME_EPOCH_MS IS NULL)
        OR
        (LAST_EVENT_TIME IS NOT NULL AND LAST_EVENT_TIME_EPOCH_MS IS NOT NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_SYNC_PROGRESS_03 CHECK (
        (LAST_BATCH_TIME IS NULL AND LAST_BATCH_TIME_EPOCH_MS IS NULL)
        OR
        (LAST_BATCH_TIME IS NOT NULL AND LAST_BATCH_TIME_EPOCH_MS IS NOT NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_SYNC_PROGRESS_04 CHECK (
        PROCESSED_ROWS >= 0
        AND SUCCESS_ROWS >= 0
        AND FAILED_ROWS >= 0
    )
);

CREATE TABLE IF NOT EXISTS SYS_VECTOR_SYNC_ERRORS_ (
    ERROR_ID BIGINT NOT NULL,
    JOB_ID BIGINT NOT NULL,
    COLUMN_ID BIGINT NOT NULL,
    PARTITION_ID VARCHAR(64),
    SOURCE_PK VARCHAR(512) NOT NULL,
    OP_TYPE VARCHAR(16) NOT NULL,
    ERROR_STAGE VARCHAR(32) NOT NULL,
    ERROR_CODE VARCHAR(64) NOT NULL,
    ERROR_MESSAGE VARCHAR(2048) NOT NULL,
    PAYLOAD_SNAPSHOT VARCHAR(2000),
    DEDUPE_KEY VARCHAR(256) NOT NULL,
    FIRST_SEEN_AT TIMESTAMP NOT NULL,
    LAST_SEEN_AT TIMESTAMP NOT NULL,
    LAST_SEEN_AT_EPOCH_MS BIGINT NOT NULL,
    RETRY_COUNT INTEGER NOT NULL,
    NEXT_RETRY_AT TIMESTAMP,
    NEXT_RETRY_AT_EPOCH_MS BIGINT,
    ERROR_STATUS VARCHAR(16) NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    PRIMARY KEY (ERROR_ID),
    CONSTRAINT UQ_SYS_VECTOR_SYNC_ERRORS_01 UNIQUE (DEDUPE_KEY),
    CONSTRAINT FK_SYS_VECTOR_SYNC_ERRORS_01 FOREIGN KEY (JOB_ID) REFERENCES SYS_VECTOR_SYNC_JOBS_ (JOB_ID),
    CONSTRAINT FK_SYS_VECTOR_SYNC_ERRORS_02 FOREIGN KEY (COLUMN_ID) REFERENCES SYS_VECTOR_COLUMNS_ (COLUMN_ID),
    CONSTRAINT CK_SYS_VECTOR_SYNC_ERRORS_01 CHECK (ERROR_STATUS IN ('OPEN', 'RETRYING', 'RESOLVED', 'DEAD')),
    CONSTRAINT CK_SYS_VECTOR_SYNC_ERRORS_02 CHECK (
        (NEXT_RETRY_AT IS NULL AND NEXT_RETRY_AT_EPOCH_MS IS NULL)
        OR
        (NEXT_RETRY_AT IS NOT NULL AND NEXT_RETRY_AT_EPOCH_MS IS NOT NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_SYNC_ERRORS_03 CHECK (
        ERROR_STATUS = 'RETRYING'
        OR
        (NEXT_RETRY_AT IS NULL AND NEXT_RETRY_AT_EPOCH_MS IS NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_SYNC_ERRORS_04 CHECK (
        RETRY_COUNT >= 0
    )
);

