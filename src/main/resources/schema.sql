CREATE TABLE IF NOT EXISTS SYS_VECTOR_OUTBOX_EVENTS_ (
    EVENT_ID BIGINT NOT NULL,
    TENANT_ID VARCHAR(64) NOT NULL,
    COLUMN_ID BIGINT NOT NULL,
    EVENT_KEY VARCHAR(768) NOT NULL,
    ACTIVE_KEY VARCHAR(768),
    EVENT_TYPE VARCHAR(16) NOT NULL,
    SOURCE_OP VARCHAR(16) NOT NULL,
    EVENT_STATUS VARCHAR(16) NOT NULL,
    SOURCE_PK VARCHAR(512) NOT NULL,
    POINT_ID VARCHAR(128) NOT NULL,
    PK_VALUE_TYPE VARCHAR(16) NOT NULL,
    DEDUPE_KEY VARCHAR(1024) NOT NULL,
    SOURCE_VERSION BIGINT NOT NULL,
    NEEDS_RESYNC CHAR(1) NOT NULL,
    RETRY_COUNT INTEGER NOT NULL,
    NEXT_RETRY_AT TIMESTAMP,
    NEXT_RETRY_AT_EPOCH_MS BIGINT,
    LOCKED_BY VARCHAR(128),
    LOCKED_AT TIMESTAMP,
    LOCKED_AT_EPOCH_MS BIGINT,
    CLAIM_TOKEN VARCHAR(64),
    FINISHED_AT TIMESTAMP,
    FINISHED_AT_EPOCH_MS BIGINT,
    ERROR_CODE VARCHAR(64),
    ERROR_MESSAGE VARCHAR(2048),
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    PRIMARY KEY (EVENT_ID),
    CONSTRAINT UQ_SYS_VECTOR_OUTBOX_EVENTS_01 UNIQUE (DEDUPE_KEY),
    CONSTRAINT UQ_SYS_VECTOR_OUTBOX_EVENTS_02 UNIQUE (ACTIVE_KEY),
    CONSTRAINT FK_SYS_VECTOR_OUTBOX_EVENTS_01 FOREIGN KEY (COLUMN_ID) REFERENCES SYS_VECTOR_COLUMNS_ (COLUMN_ID),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_01 CHECK (EVENT_TYPE IN ('UPSERT', 'DELETE')),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_02 CHECK (SOURCE_OP IN ('INSERT', 'UPDATE', 'DELETE')),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_03 CHECK (EVENT_STATUS IN ('PENDING', 'PROCESSING', 'DONE', 'RETRYING', 'DEAD')),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_04 CHECK (PK_VALUE_TYPE IN ('NUMBER', 'STRING')),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_05 CHECK (RETRY_COUNT >= 0),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_06 CHECK (
        (NEXT_RETRY_AT IS NULL AND NEXT_RETRY_AT_EPOCH_MS IS NULL)
        OR
        (NEXT_RETRY_AT IS NOT NULL AND NEXT_RETRY_AT_EPOCH_MS IS NOT NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_07 CHECK (
        (LOCKED_AT IS NULL AND LOCKED_AT_EPOCH_MS IS NULL)
        OR
        (LOCKED_AT IS NOT NULL AND LOCKED_AT_EPOCH_MS IS NOT NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_08 CHECK (
        (FINISHED_AT IS NULL AND FINISHED_AT_EPOCH_MS IS NULL)
        OR
        (FINISHED_AT IS NOT NULL AND FINISHED_AT_EPOCH_MS IS NOT NULL)
    ),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_09 CHECK (SOURCE_VERSION > 0),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_10 CHECK (NEEDS_RESYNC IN ('Y', 'N')),
    CONSTRAINT CK_SYS_VECTOR_OUTBOX_EVENTS_11 CHECK (
        (EVENT_STATUS = 'DONE' AND ACTIVE_KEY IS NULL)
        OR
        (EVENT_STATUS <> 'DONE' AND ACTIVE_KEY IS NOT NULL)
    )
);

CREATE TABLE IF NOT EXISTS SYS_VECTOR_SOURCE_VERSIONS_ (
