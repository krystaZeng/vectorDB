CREATE SEQUENCE IF NOT EXISTS SYS_VECTOR_ID_SEQ
    START WITH 1
    INCREMENT BY 1;

CREATE TABLE IF NOT EXISTS SYS_VECTOR_COLUMNS_ (
    COLUMN_ID BIGINT NOT NULL,
    TENANT_ID VARCHAR(64) NOT NULL,
    SCHEMA_NAME VARCHAR(128) NOT NULL,
    TABLE_NAME VARCHAR(128) NOT NULL,
    COLUMN_NAME VARCHAR(128) NOT NULL,
    PK_COLUMN_NAME VARCHAR(128) NOT NULL,
    VECTOR_DIM INTEGER NOT NULL,
    METRIC_TYPE VARCHAR(16) NOT NULL,
    VECTOR_ENCODING VARCHAR(32) NOT NULL,
    POINT_ID_MODE VARCHAR(16) NOT NULL,
    DELETE_POLICY VARCHAR(16) NOT NULL,
    STATUS VARCHAR(16) NOT NULL,
    SYNC_MODE VARCHAR(32) NOT NULL,
    DEFINITION_HASH VARCHAR(64) NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    REMARK VARCHAR(1024),
    PRIMARY KEY (COLUMN_ID),
    CONSTRAINT UQ_SYS_VECTOR_COLUMNS_01 UNIQUE (TENANT_ID, SCHEMA_NAME, TABLE_NAME, COLUMN_NAME),
    CONSTRAINT CK_SYS_VECTOR_COLUMNS_01 CHECK (STATUS IN ('BUILDING', 'ACTIVE', 'FAILED', 'DISABLED')),
    CONSTRAINT CK_SYS_VECTOR_COLUMNS_02 CHECK (SYNC_MODE IN ('FULL_AND_INCREMENTAL', 'FULL_ONLY'))
);

CREATE TABLE IF NOT EXISTS SYS_VECTOR_COLLECTIONS_ (
    COLLECTION_ID BIGINT NOT NULL,
    COLUMN_ID BIGINT NOT NULL,
    ENGINE_TYPE VARCHAR(16) NOT NULL,
    NAMESPACE_NAME VARCHAR(128) NOT NULL,
    COLLECTION_NAME VARCHAR(256) NOT NULL,
    ALIAS_NAME VARCHAR(256),
    COLLECTION_VERSION VARCHAR(64) NOT NULL,
    SERVING_STATE VARCHAR(16) NOT NULL,
    COLLECTION_STATUS VARCHAR(16) NOT NULL,
    QDRANT_VECTOR_NAME VARCHAR(128) NOT NULL,
    QDRANT_ID_TYPE VARCHAR(16) NOT NULL,
    DISTANCE_METRIC VARCHAR(16) NOT NULL,
    VECTOR_DIM INTEGER NOT NULL,
    SHARD_NUMBER INTEGER,
    REPLICATION_FACTOR INTEGER,
    WRITE_CONSISTENCY_FACTOR INTEGER,
    ON_DISK_PAYLOAD CHAR(1) NOT NULL,
    HNSW_CONFIG_JSON VARCHAR(4000),
    QUANTIZATION_CONFIG_JSON VARCHAR(4000),
    COLLECTION_CONFIG_JSON VARCHAR(4000),
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    PRIMARY KEY (COLLECTION_ID),
    CONSTRAINT UQ_SYS_VECTOR_COLLECTIONS_01 UNIQUE (ENGINE_TYPE, NAMESPACE_NAME, COLLECTION_NAME),
    CONSTRAINT UQ_SYS_VECTOR_COLLECTIONS_02 UNIQUE (COLUMN_ID, COLLECTION_VERSION),
    CONSTRAINT FK_SYS_VECTOR_COLLECTIONS_01 FOREIGN KEY (COLUMN_ID) REFERENCES SYS_VECTOR_COLUMNS_ (COLUMN_ID),
    CONSTRAINT CK_SYS_VECTOR_COLLECTIONS_01 CHECK (SERVING_STATE IN ('BUILDING', 'ACTIVE', 'DEPRECATED')),
    CONSTRAINT CK_SYS_VECTOR_COLLECTIONS_02 CHECK (COLLECTION_STATUS IN ('CREATING', 'READY', 'FAILED', 'DROPPED')),
    CONSTRAINT CK_SYS_VECTOR_COLLECTIONS_03 CHECK (
        VECTOR_DIM > 0
        AND (SHARD_NUMBER IS NULL OR SHARD_NUMBER > 0)
        AND (REPLICATION_FACTOR IS NULL OR REPLICATION_FACTOR > 0)
        AND (WRITE_CONSISTENCY_FACTOR IS NULL OR WRITE_CONSISTENCY_FACTOR > 0)
    ),
    CONSTRAINT CK_SYS_VECTOR_COLLECTIONS_04 CHECK (
        (SERVING_STATE = 'BUILDING' AND COLLECTION_STATUS IN ('CREATING', 'FAILED'))
        OR (SERVING_STATE = 'ACTIVE' AND COLLECTION_STATUS = 'READY')
        OR (SERVING_STATE = 'DEPRECATED' AND COLLECTION_STATUS IN ('READY', 'DROPPED'))
    )
);

CREATE TABLE IF NOT EXISTS SYS_VECTOR_INDEXES_ (
    INDEX_ID BIGINT NOT NULL,
    COLUMN_ID BIGINT NOT NULL,
    COLLECTION_ID BIGINT,
    PROFILE_NAME VARCHAR(128) NOT NULL,
    INDEX_TYPE VARCHAR(32) NOT NULL,
    METRIC_TYPE VARCHAR(16) NOT NULL,
    INDEX_PARAMS_JSON VARCHAR(4000),
    SEARCH_PARAMS_JSON VARCHAR(4000),
    PAYLOAD_INDEX_JSON VARCHAR(4000),
    IS_DEFAULT CHAR(1) NOT NULL,
    SERVING_STATE VARCHAR(16) NOT NULL,
    INDEX_STATUS VARCHAR(16) NOT NULL,
    BUILD_VERSION VARCHAR(64),
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    PRIMARY KEY (INDEX_ID),
    CONSTRAINT UQ_SYS_VECTOR_INDEXES_01 UNIQUE (COLUMN_ID, PROFILE_NAME),
    CONSTRAINT FK_SYS_VECTOR_INDEXES_01 FOREIGN KEY (COLUMN_ID) REFERENCES SYS_VECTOR_COLUMNS_ (COLUMN_ID),
    CONSTRAINT FK_SYS_VECTOR_INDEXES_02 FOREIGN KEY (COLLECTION_ID) REFERENCES SYS_VECTOR_COLLECTIONS_ (COLLECTION_ID),
    CONSTRAINT CK_SYS_VECTOR_INDEXES_01 CHECK (IS_DEFAULT IN ('Y', 'N')),
    CONSTRAINT CK_SYS_VECTOR_INDEXES_02 CHECK (SERVING_STATE IN ('ONLINE', 'OFFLINE', 'CANARY')),
    CONSTRAINT CK_SYS_VECTOR_INDEXES_03 CHECK (INDEX_STATUS IN ('CREATING', 'READY', 'FAILED', 'REBUILDING')),
    CONSTRAINT CK_SYS_VECTOR_INDEXES_04 CHECK (COLLECTION_ID IS NULL OR COLLECTION_ID > 0),
    CONSTRAINT CK_SYS_VECTOR_INDEXES_05 CHECK (
        (SERVING_STATE = 'ONLINE' AND INDEX_STATUS = 'READY')
        OR (SERVING_STATE = 'OFFLINE' AND INDEX_STATUS IN ('CREATING', 'READY', 'FAILED', 'REBUILDING'))
        OR (SERVING_STATE = 'CANARY' AND INDEX_STATUS = 'READY')
    )
);

CREATE TABLE IF NOT EXISTS SYS_VECTOR_PAYLOAD_FIELDS_ (
